/*
        Terme et Définition
        string = chaîne de caractères
        Une séquence de caractères utilisée pour représenter du texte. En JavaScript, les chaînes de caractères peuvent être délimitées par des guillemets simples (' ') ou doubles (" ").

        variable = variable
        Un conteneur permettant de stocker des valeurs. Une variable peut être déclarée avec les mots-clés var, let, ou const en JavaScript.

        integer = entier
        Un type de données représentant des nombres entiers (positifs ou négatifs), sans partie fractionnaire.

        boolean = booléen
        Un type de données ayant deux valeurs possibles : true (vrai) ou false (faux).

        array = tableau
        Une structure de données qui permet de stocker plusieurs valeurs dans une seule variable. Les valeurs sont accessibles par des indices.

        object = objet
        Une collection de propriétés, où chaque propriété est définie par une clé (ou nom) et une valeur. Les objets permettent de regrouper des données et des fonctionnalités liées.

        function = fonction
        Un bloc de code réutilisable qui exécute une tâche spécifique. Les fonctions peuvent prendre des arguments et retourner des valeurs.

        loop = boucle
        Une structure de contrôle qui permet de répéter l'exécution d'un bloc de code plusieurs fois. Les boucles courantes en JavaScript incluent for, while, et do...while.

        condition = condition
        Une expression qui évalue à true (vrai) ou false (faux) et détermine le flux d'exécution du programme, souvent utilisée avec des structures if, else if, et else.

        operator = opérateur
        Un symbole ou une combinaison de symboles qui effectue une opération sur une ou plusieurs valeurs. Exemples : + (addition), - (soustraction), * (multiplication), / (division), == (égal à), != (différent de).

        statement = instruction
        Une ligne de code qui effectue une action spécifique, comme déclarer une variable ou appeler une fonction.

        expression = expression
        Une combinaison de valeurs, de variables, d'opérateurs et de fonctions qui sont évaluées pour produire une valeur.

        parameter = paramètre
        Une variable utilisée pour passer des données à une fonction. Les paramètres sont spécifiés dans la définition de la fonction.

        argument = argument
        Une valeur fournie à une fonction lors de son appel. Les arguments correspondent aux paramètres définis par la fonction.

        scope = portée
        Le contexte dans lequel une variable ou une fonction est accessible. Les portées courantes incluent la portée globale et la portée locale (à l'intérieur d'une fonction ou d'un bloc de code).

        event = événement
        Une action ou une occurrence détectée par le programme, souvent générée par l'utilisateur (comme un clic de souris) ou par le système. Les événements peuvent déclencher l'exécution de fonctions.

        callback = fonction de rappel
        Une fonction passée en argument à une autre fonction et qui est exécutée après que cette dernière a terminé son action.
*/